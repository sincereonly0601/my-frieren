def main() -> None:
    if len(STEM_CORES) != 110:
        raise SystemExit(f"STEM_CORES length {len(STEM_CORES)} != 110")
    frieren_f, whim_f = _fonts()
    data = json.loads(JSON_PATH.read_text(encoding="utf-8"))
    if len(data) != 110:
        raise SystemExit(f"JSON length {len(data)} != 110")
    for i, item in enumerate(data):
        item["stem"] = _compose_stem_display(frieren_f, whim_f, i, STEM_CORES[i])
    bad: list[str] = []
    for i, item in enumerate(data):
        s = item["stem"]
        nf = len(_wrap_cjk(frieren_f, s, _TW_F))
        wrapped_whim = _wrap_cjk(whim_f, f"【{s}】", _TEXT_W_WHIM)
        nw = len(wrapped_whim)
        if nf != 2:
            bad.append(f"#{i+1} frieren_lines={nf} (want 2): {s[:48]}…")
        if nw > 2:
            bad.append(f"#{i+1} whim_lines={nw} (max 2): {s[:48]}…")
    if bad:
        print("\n".join(bad))
        raise SystemExit(1)
    JSON_PATH.write_text(
        json.dumps(data, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print("OK: wrote", JSON_PATH, "— all stems frieren=2 lines, whim<=2 lines")


if __name__ == "__main__":
    main()
