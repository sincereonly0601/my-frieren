_COL_GAP = 8 * CW // 320
_PANEL_W = 100 * CW // 320
_TEXT_W_WHIM = max(40, CW - (_MX + _PANEL_W + _COL_GAP) - _MX)
_FONT_NAMES = (
    "microsoftyaheiui",
    "microsoftyahei",
    "pingfang sc",
    "noto sans cjk tc",
    "simsun",
)


def _wrap_cjk(font: pygame.font.Font, text: str, max_width: int) -> list[str]:
    if max_width <= 0:
        return [text] if text else [""]
    lines: list[str] = []
    current = ""
    for ch in text:
        trial = current + ch
        if font.size(trial)[0] <= max_width:
            current = trial
        else:
            if current:
                lines.append(current)
            current = ch
    if current:
        lines.append(current)
    return lines if lines else [""]


def _fonts() -> tuple[pygame.font.Font, pygame.font.Font]:
    pygame.font.init()
    frieren = pygame.font.SysFont(",".join(_FONT_NAMES), 22)
    whim_px = max(18, min(21, CH // 26))
    whim = pygame.font.SysFont(",".join(_FONT_NAMES), whim_px)
    return frieren, whim


