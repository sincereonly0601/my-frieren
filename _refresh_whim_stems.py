def _compose_stem_display(
    frieren_font: pygame.font.Font,
    whim_font: pygame.font.Font,
    index: int,
    core: str,
) -> str:
    """
    組合題庫顯示用題幹：核心在芙莉蓮寬度下已自然兩行則維持；否則輪替接上情境前綴，直至同時滿足芙莉蓮兩行、奇遇【】不超過兩行。

    Args:
        frieren_font: 芙莉蓮測驗主文（22px）量測用。
        whim_font: 奇遇測驗 intro 字級量測用。
        index: 題號 0-based（決定前綴輪替起點）。
        core: 題幹內文核心。

    Returns:
        寫入 JSON 的完整 stem。

    Raises:
        RuntimeError: 無法找到符合版面之前綴組合時。
    """
    core = core.strip()
    if not core:
        raise RuntimeError("empty stem core")
    nf0 = len(_wrap_cjk(frieren_font, core, _TW_F))
    nw0 = len(_wrap_cjk(whim_font, f"【{core}】", _TEXT_W_WHIM))
    if nf0 == 2 and nw0 <= 2:
        return core
    n_pref = len(_ALL_PREFIXES)
    rotated = _ALL_PREFIXES[index % n_pref :] + _ALL_PREFIXES[: index % n_pref]
    for fl in rotated:
        cand = f"{fl}{core}"
        if (
            len(_wrap_cjk(frieren_font, cand, _TW_F)) == 2
            and len(_wrap_cjk(whim_font, f"【{cand}】", _TEXT_W_WHIM)) <= 2
        ):
            return cand
    raise RuntimeError(f"stem layout fail index={index} core={core[:40]}…")


