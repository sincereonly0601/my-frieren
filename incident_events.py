def _incident_aftermath_with_frieren_ambience(
    body: tuple[str, ...],
    tier: int,
    event_id: str,
    option_index: int,
) -> tuple[str, ...]:
    """
    在表冊第一段末接三句、第二段末接兩句世界氛圍補綴，拉長餘韻版面並維持葬送風奇幻基調。

    Args:
        body: ``INCIDENT_AFTERMATH_BY_ID`` 之單選項段落元組（通常兩段）。
        tier: 1～3。
        event_id: 事件 id。
        option_index: 選項 0～2。

    Returns:
        與 ``body`` 同長度之元組；第一段末為 ``sa``＋``sc``＋``se``，第二段末為 ``sb``＋``sd``。
    """
    pools = (
        (
            _INC_AMB_T1_A,
            _INC_AMB_T1_B,
            _INC_AMB_T1_C,
            _INC_AMB_T1_D,
            _INC_AMB_T1_E,
        ),
        (
            _INC_AMB_T2_A,
            _INC_AMB_T2_B,
            _INC_AMB_T2_C,
            _INC_AMB_T2_D,
            _INC_AMB_T2_E,
        ),
        (
            _INC_AMB_T3_A,
            _INC_AMB_T3_B,
            _INC_AMB_T3_C,
            _INC_AMB_T3_D,
            _INC_AMB_T3_E,
        ),
    )[max(1, min(3, tier)) - 1]
    a_pool, b_pool, c_pool, d_pool, e_pool = pools
    ha = zlib.adler32(f"{event_id}\0{option_index}\0ambA".encode("utf-8"))
    hb = zlib.adler32(f"{event_id}\0{option_index}\0ambB".encode("utf-8"))
    hc = zlib.adler32(f"{event_id}\0{option_index}\0ambC".encode("utf-8"))
    hd = zlib.adler32(f"{event_id}\0{option_index}\0ambD".encode("utf-8"))
    he = zlib.adler32(f"{event_id}\0{option_index}\0ambE".encode("utf-8"))
    sa = a_pool[ha % len(a_pool)]
    sb = b_pool[hb % len(b_pool)]
    sc = c_pool[hc % len(c_pool)]
    sd = d_pool[hd % len(d_pool)]
    se = e_pool[he % len(e_pool)]
    parts = list(body)
    if not parts:
        return body
    parts[0] = parts[0] + sa + sc + se
    if len(parts) >= 2:
        parts[1] = parts[1] + sb + sd
    return tuple(parts)


