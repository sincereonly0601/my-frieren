@dataclass(frozen=True)
class IncidentOption:
    """
    突發事件單一選項。

    Attributes:
        label: 選項文字。
        deltas: 僅含五維鍵；數值加總必為 +10。
        aftermath: 確認選項後顯示的餘韻段落（固定兩段；與重大事件結語同調）。
    """

    label: str
    deltas: dict[str, int]
    aftermath: tuple[str, ...] = ()


@dataclass(frozen=True)
class IncidentEvent:
    """
    一則突發事件。

    Attributes:
        id: 唯一鍵。
        tier: 1＝滿 4～7 歲池、2＝9～12、3＝14～17。
        title: 標題。
        body: 敘述（可多行，繪製時會換行）。
        options: 固定三個選項。
    """

    id: str
    tier: int
    title: str
    body: str
    options: tuple[IncidentOption, IncidentOption, IncidentOption]


def _io(label: str, **kw: int) -> IncidentOption:
    """建立選項並斷言五維加總為 +10。"""
    d = {k: int(v) for k, v in kw.items() if v != 0}
    for k in d:
        if k not in _INCIDENT_STAT_KEYS:
            raise ValueError(f"invalid stat key: {k}")
    s = sum(d.values())
    if s != 10:
        raise ValueError(f"option net must be +10, got {s}: {label!r}")
    return IncidentOption(label, d, ())


def incident_tier_for_age_year(age_years: int) -> int | None:
    """
    由滿歲年齡對應突發事件池 tier。

    Args:
        age_years: 滿歲整數（年）。

    Returns:
        1、2、3，或不在觸發範圍則 None。
    """
    if age_years in (4, 5, 6, 7):
        return 1
    if age_years in (9, 10, 11, 12):
        return 2
    if age_years in (14, 15, 16, 17):
        return 3
    return None


def pick_random_incident(
    age_years: int,
    rng: random.Random,
    exclude_ids: set[str] | frozenset[str] | None = None,
) -> IncidentEvent | None:
    """
    自符合年齡層的池子隨機取一則事件（可排除已發生過的 id）。

    Args:
        age_years: 本次滿足的滿歲年齡。
        rng: 亂數來源。
        exclude_ids: 本次遊玩已觸發過的事件 id；有則自池中剔除。

    Returns:
        事件；若無對應池或剔除後為空則 None。
    """
    tier = incident_tier_for_age_year(age_years)
    if tier is None:
        return None
    pool = [e for e in ALL_INCIDENTS if e.tier == tier]
    if exclude_ids:
        pool = [e for e in pool if e.id not in exclude_ids]
    if not pool:
        return None
    return rng.choice(pool)


def format_incident_deltas_brief(deltas: dict[str, int]) -> str:
    """
    將選項增減化為簡短中文（用於提示）。

    Args:
        deltas: 五維增量。

    Returns:
        例如「智力+8  務實+2」。
    """
    bits = [
        f"{STAT_LABEL_ZH.get(k, k)}{'+' if v > 0 else ''}{v}"
        for k, v in sorted(deltas.items())
        if v != 0 and k in _INCIDENT_STAT_KEYS
    ]
    return "  ".join(bits) if bits else "（無變化）"


