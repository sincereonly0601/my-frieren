def _incident_reflection_coda(event_id: str, option_index: int, tier: int) -> str:
    """
    依事件與選項決定性挑選結尾反思句（同一存檔路線可重現）。

    Args:
        event_id: ``IncidentEvent.id``。
        option_index: 選項 0～2。
        tier: 1～3。

    Returns:
        一句反思；由 ``_aftermath_two_paragraphs_with_coda`` 併入餘韻第二段末尾。
    """
    h = zlib.adler32(f"{event_id}\0{option_index}".encode("utf-8"))
    pool = (_INCIDENT_REFLECTION_CODAS_T1, _INCIDENT_REFLECTION_CODAS_T2, _INCIDENT_REFLECTION_CODAS_T3)[
        tier - 1
    ]
    return pool[h % len(pool)]


