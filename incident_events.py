def _aftermath_two_paragraphs_with_coda(
    body: tuple[str, ...], coda: str,
) -> tuple[str, ...]:
    """
    將表冊段落與反思句收成恰好兩段（反思接於最末表冊段之後）。

    Args:
        body: ``INCIDENT_AFTERMATH_BY_ID`` 該選項之段落元組。
        coda: ``_incident_reflection_coda`` 產生之結尾句。

    Returns:
        長度 1～2 之段落元組；表冊空時僅 ``(coda,)``。
    """
    if not body:
        return (coda,)
    if len(body) == 1:
        return (body[0], coda)
    first = "".join(body[:-1])
    return (first, body[-1] + coda)


# 單頁餘韻至少約六行：合計字數低於此則於末段末追加補綴（約 36 全形字／行 × 6 之保守下限）
_INCIDENT_AFTERMATH_MIN_TOTAL_CHARS: Final[int] = 260

_INCIDENT_AFTERMATH_LENGTH_PADS: Final[tuple[str, ...]] = (
    "妳望向遠方與腳下同時存在的世界，知道選擇很少當下就顯出全部重量，卻會在多年後浮上來，成為辨認自己的刻度。",
    "風穿過草原與石牆之間，鐘聲與腳步交疊——旅路還長，妳把今日的餘溫收進衣袖，預留給下一個需要耐心的季節。",
)


def _pad_incident_aftermath_min_chars(aftermath: tuple[str, ...]) -> tuple[str, ...]:
    """
    若餘韻合計字數過短，於末段末依序拼接補綴句，使單頁顯示至少約六行。

    Args:
        aftermath: 已含氛圍與反思之段落元組。

    Returns:
        可能加長後之元組。
    """
    if not aftermath:
        return aftermath
    parts = list(aftermath)
    total = sum(len(p) for p in parts)
    i = 0
    while (
        total < _INCIDENT_AFTERMATH_MIN_TOTAL_CHARS
        and i < len(_INCIDENT_AFTERMATH_LENGTH_PADS)
    ):
        parts[-1] = parts[-1] + _INCIDENT_AFTERMATH_LENGTH_PADS[i]
        total = sum(len(p) for p in parts)
        i += 1
    return tuple(parts)


def _apply_incident_aftermath(events: tuple[IncidentEvent, ...]) -> tuple[IncidentEvent, ...]:
    """
    依事件 id 為三個選項套上餘韻段落（單頁兩段呈現，字數不足時補綴）。

    Args:
        events: 原始突發事件（選項尚無 aftermath）。

    Returns:
        合併餘韻後的新 tuple。
    """
    out: list[IncidentEvent] = []
    for e in events:
        bodies = INCIDENT_AFTERMATH_BY_ID[e.id]
        out.append(
            IncidentEvent(
                e.id,
                e.tier,
                e.title,
                e.body,
                tuple(
                    IncidentOption(
                        o.label,
                        o.deltas,
                        _pad_incident_aftermath_min_chars(
                            _aftermath_two_paragraphs_with_coda(
                                _incident_aftermath_with_frieren_ambience(
                                    bodies[i], e.tier, e.id, i
                                ),
                                _incident_reflection_coda(e.id, i, e.tier),
                            )
                        ),
                    )
                    for i, o in enumerate(e.options)
                ),
            )
        )
    return tuple(out)


